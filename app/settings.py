class BracketLocationFormatter(logging.Formatter):
    def __init__(self, format=None, datefmt=None, style="%", validate=True, **kwargs):
        super().__init__(fmt=format, datefmt=datefmt, style=style, validate=validate)

    def format(self, record):
        record.location = f"[{record.name}:{record.lineno}]"
        return super().format(record)


